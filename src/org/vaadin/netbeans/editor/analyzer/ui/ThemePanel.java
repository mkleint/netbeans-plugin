    private void selectText() {
        SwingUtilities.invokeLater(new Runnable() {

            @Override
            public void run() {
                if (SwingUtilities.getWindowAncestor(ThemePanel.this) == null) {
                    selectText();
                }
                else {
                    myThemeName.requestFocusInWindow();
                    myThemeName.selectAll();
                }
            }
        });
    }

